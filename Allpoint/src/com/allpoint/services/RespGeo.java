package com.allpoint.services;

public class RespGeo {

	public String GeoStatus;
	public String GeoStatusCode;
	public String GeoStatusMessage;
	public String GeoUserId;
	public String GeoFirstName;
	public String GeoEmailId;
	public String GeoZipCode;

	public String getGeoStatus() {
		return GeoStatus;
	}

	public void setGeoStatus(String geoStatus) {
		GeoStatus = geoStatus;
	}

	public String getGeoStatusCode() {
		return GeoStatusCode;
	}

	public void setGeoStatusCode(String geoStatusCode) {
		GeoStatusCode = geoStatusCode;
	}

	public String getGeoStatusMessage() {
		return GeoStatusMessage;
	}

	public void setGeoStatusMessage(String geoStatusMessage) {
		GeoStatusMessage = geoStatusMessage;
	}

	public String getGeoUserId() {
		return GeoUserId;
	}

	public void setGeoUserId(String geoUserId) {
		GeoUserId = geoUserId;
	}

	public String getFirstName() {
		return GeoFirstName;
	}

	public void setFirstName(String geoFirstName) {
		GeoFirstName = geoFirstName;
	}

	public String getEmailId() {
		return GeoEmailId;
	}

	public void setEmailId(String geoEmailId) {
		GeoEmailId = geoEmailId;
	}

	public String getZipCode() {
		return GeoZipCode;
	}

	public void setZipCode(String geoZipCode) {
		GeoZipCode = geoZipCode;
	}

}
